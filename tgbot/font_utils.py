def build_font_map(
    upper: str | None = None,
    lower: str | None = None,
    digits: str | None = None,
) -> dict[str, str]:
    mapping: dict[str, str] = {}
    if upper:
        mapping.update(dict(zip("ABCDEFGHIJKLMNOPQRSTUVWXYZ", upper)))
    if lower:
        mapping.update(dict(zip("abcdefghijklmnopqrstuvwxyz", lower)))
    if digits:
        mapping.update(dict(zip("0123456789", digits)))
    return mapping


