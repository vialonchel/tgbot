def apply_font_style(text: str, style_id: str) -> str:
    if style_id == "combining_glitch":
        marks = ["Í›", "Í‘", "Ì“", "Ì½", "Ìš", "Ì’", "Ì•", "ÍŒ", "Í†", "Í", "Í˜", "Í", "Ì¾", "Í„"]
        out: list[str] = []
        for i, ch in enumerate(text):
            if ch.isspace():
                out.append(ch)
                continue
            out.append(ch + marks[i % len(marks)] + marks[(i + 3) % len(marks)])
        return "".join(out)
    if style_id in {"overline", "underline", "strikethrough"}:
        mark = {
            "overline": "\u0305",
            "underline": "\u0332",
            "strikethrough": "\u0336",
        }[style_id]
        out: list[str] = []
        for ch in text:
            if ch.isspace():
                out.append(ch)
                continue
            out.append(ch + mark)
        return "".join(out)

    mapping = FONT_MAPPINGS.get(style_id)
    if not mapping:
        return text
    return "".join(mapping.get(ch, ch) for ch in text)
